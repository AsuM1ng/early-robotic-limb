/**********************************************************
* @FuncName: Filter_MovingAverage
* @Param: *pFilterBuf -- 滤波队列，输入为数组名
*			   CurVal -- 当前输入值，插入对尾
*					n -- 队列长度
* @Return: 本次滤波输出
* @Brief: 滤波器特点：
* @addtion:
**********************************************************/
int32_t Filter_MovingAverage(int32_t *pFilterBuf, const int32_t CurVal, const int32_t n)
{
	int16_t i = 0;
	int32_t Sum = 0;
	
	for(i = 0; i < n - 1; i++)
	{
		pFilterBuf[i] = pFilterBuf[i+1];
		Sum += pFilterBuf[i];
	}
	pFilterBuf[i] = CurVal;
	Sum += pFilterBuf[i];
	
	return (Sum / n);
	
}



