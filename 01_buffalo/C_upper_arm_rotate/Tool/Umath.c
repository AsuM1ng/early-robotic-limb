uint32_t UM_IntSqrt(uint32_t x)
{

   uint32_t temp = 0;
   uint32_t v_bit = 15;
   uint32_t n = 0;
   uint32_t b = 0x8000;

   if (x <= 1)
       return x;

   do{
       temp = ((n << 1) + b) << (v_bit--);
       if (x >= temp)
       {
           n += b;
           x -= temp;
       }
   }while (b >>= 1);
   
   return n; 
}

